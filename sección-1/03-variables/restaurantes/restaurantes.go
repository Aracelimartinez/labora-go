package main

import "fmt"

func main() {
	restaurantName := "Mac"
	rating := 4.0
	fmt.Printf("El restaurante %s tiene una calificación de %v estrellas.\n", restaurantName, rating)

	restaurantName = "BK"
	rating = 4.0
	fmt.Printf("El restaurante %s tiene una calificación de %v estrellas.\n", restaurantName, rating)

	restaurantName = "Subway"
	rating = 4.5
	fmt.Printf("El restaurante %s tiene una calificación de %v estrellas.\n", restaurantName, rating)

}
